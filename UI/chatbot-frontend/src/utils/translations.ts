export const getTranslation = (key: string, language: string): string => {
  return translations[language]?.[key] || translations.en[key] || key;
};

export const useTranslation = (language: string) => {
  return {
    t: (key: string) => getTranslation(key, language)
  };
};

// Thêm utility functions cho date formatting
export const formatDate = (date: Date, language: string, format: 'short' | 'long' = 'short'): string => {
  const dayNames = format === 'short' 
    ? ['sun', 'mon', 'tue', 'wed', 'thu', 'fri', 'sat']
    : ['sunday', 'monday', 'tuesday', 'wednesday', 'thursday', 'friday', 'saturday'];
  
  const monthNames = format === 'short'
    ? ['jan', 'feb', 'mar', 'apr', 'may', 'jun', 'jul', 'aug', 'sep', 'oct', 'nov', 'dec']
    : ['january', 'february', 'march', 'april', 'may', 'june', 'july', 'august', 'september', 'october', 'november', 'december'];
  
  const dayName = getTranslation(`time.${dayNames[date.getDay()]}`, language);
  const monthName = getTranslation(`time.${monthNames[date.getMonth()]}`, language);
  
  if (format === 'short') {
    return `${dayName}`;
  } else {
    return `${dayName}, ${date.getDate()} ${monthName} ${date.getFullYear()}`;
  }
};

export const formatMonthDay = (date: Date, language: string): string => {
  const monthNames = ['jan', 'feb', 'mar', 'apr', 'may', 'jun', 'jul', 'aug', 'sep', 'oct', 'nov', 'dec'];
  const monthName = getTranslation(`time.${monthNames[date.getMonth()]}`, language);
  return `${monthName} ${date.getDate()}`;
};
